// calculo del área de un rectángulo
package main
import (
        "fmt"
        )
func main() {
    var largo float64 = 3.5 // Declarar variable largo como float64 y se le asigna el valor 3.5
    var ancho = 3.8      // Declarar variable ancho como float64 y se le asigna valor 3.8
    fmt.Println("\n\nArea del rectángulo de largo",largo,"mts y ancho",ancho,"mts es:",largo*ancho,"mts cuadrados")
}
