package main
import (
        "fmt"
        "math"
        )
func main() {

  fmt.Println(math.Floor(5.345))    // parte entera o entero más grande menor qué o igual a número
  fmt.Println(math.Sin(2))          //   seno de un angulo    
  fmt.Println(math.Cos(2))          //   coseno 
  fmt.Println(math.Tan(2))          //   tangente 
  fmt.Println(math.Sqrt(2))         //   raiz cuadrada
  fmt.Println((math.Pi))            //   Valor de Pi
  fmt.Println(math.Abs(-6.892))     //   Valor abbsoluto 
  fmt.Println((math.Ceil(math.Pi))) //   entero más pequeño que sea mayor qué o igual al número 
  
}    

